    private void tampil() throws SQLException {
        int row = jTable_mahasiswa.getRowCount();
        for (int s=0; s<row; s++)
        {
            model.removeRow(0);
        }
        Connection connection = getConnection();
        ResultSet rs = connection.createStatement().executeQuery("SELECT * FROM mahasiswa");
        
        while(rs.next()){
            String data[] = {rs.getString(1), rs.getString(2), rs.getString(3), rs.getString(4)};
            model.addRow(data);
        }   
    }
    
    private void reset() {
        jTextField_npm.setText("");
        jTextField_nama.setText("");
        jComboBox_jk.setSelectedItem("");
        jTextField_tlp.setText("");
    }
    
    
