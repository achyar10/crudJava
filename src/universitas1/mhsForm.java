    private void jButton_insertActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_insertActionPerformed
        try {
            Connection connection = getConnection();
            connection.createStatement().executeUpdate("INSERT INTO mahasiswa VALUES"+"('"+jTextField_npm.getText()+"', '"+jTextField_nama.getText()+"', '"+jComboBox_jk.getSelectedItem()+"', '"+jTextField_tlp.getText()+"')");
            JOptionPane.showMessageDialog(null, "Tambah Data Mahasiswa Berhasil");
            tampil();
            reset();
        } catch (SQLException ex) {
            Logger.getLogger(mhsForm.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton_insertActionPerformed

    private void jTable_mahasiswaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable_mahasiswaMouseClicked
        int i = jTable_mahasiswa.getSelectedRow();
        if(i>-1)
        {
            jTextField_npm.setText(model.getValueAt(i,0).toString());
            jTextField_nama.setText(model.getValueAt(i,1).toString());
            jComboBox_jk.setSelectedItem(model.getValueAt(i, 2).toString());
            jTextField_tlp.setText(model.getValueAt(i,3).toString()); 
        }
    }//GEN-LAST:event_jTable_mahasiswaMouseClicked

    private void jButton_updateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_updateActionPerformed
        try {
            Connection connection = getConnection();
            connection.createStatement().executeUpdate("UPDATE mahasiswa SET mahasiswa_nama='"+jTextField_nama.getText()+"', mahasiswa_jk='"+jComboBox_jk.getSelectedItem()+"', mahasiswa_tlp='"+jTextField_tlp.getText()+"' WHERE mahasiswa_npm='"+jTextField_npm.getText()+"' ");
            JOptionPane.showMessageDialog(null, "Update Data Mahasiswa Berhasil");
            tampil();
            reset();
        } catch (SQLException ex) {
            Logger.getLogger(mhsForm.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton_updateActionPerformed

    private void jButton_deleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_deleteActionPerformed
        try {
            Connection connection = getConnection();
            connection.createStatement().executeUpdate("DELETE FROM mahasiswa WHERE mahasiswa_npm='"+jTextField_npm.getText()+"' ");
            JOptionPane.showMessageDialog(null, "Delete Data Mahasiswa Berhasil");
            tampil();
            reset();
        } catch (SQLException ex) {
            Logger.getLogger(mhsForm.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton_deleteActionPerformed

    private void jButton_cariActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_cariActionPerformed
        try {
            int row = jTable_mahasiswa.getRowCount();
            for (int s=0; s<row; s++)
            {
                model.removeRow(0);
            }
            Connection connection = getConnection();
            ResultSet rs = connection.createStatement().executeQuery("SELECT * FROM mahasiswa WHERE mahasiswa_nama LIKE '%"+jTextField_cari.getText()+"%' ");
            
            while(rs.next()){
                String data[] = {rs.getString(1), rs.getString(2), rs.getString(3), rs.getString(4)};
                model.addRow(data);
            }
        } catch (SQLException ex) {
            Logger.getLogger(mhsForm.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton_cariActionPerformed

